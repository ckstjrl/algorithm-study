import sys
input = lambda: sys.stdin.readline().rstrip()

# 트리에 경로를 추가하는 함수
def add_path(tree, path):
    current = tree  # 현재 층 = 0층(루트 노드 층)부터 시작
    for node in path:   # 경로를 이동하면서 지나간 각 node들을 순회하면서,
        if node not in current: # 현재 층에 node가 존재하지 않으면,
            current[node] = {}      # 새 하위 딕셔너리 생성
        current = current[node] # current의 node 키에 해당하는 value를 다시 current로 설정해 중첩 딕셔너리 구조를 만들며 한 층 내려가기

# 트리를 시각화된 구조로 출력하는 함수
def print_tree(tree, level=0):
    for node in sorted(tree.keys()):  # 현재 층의 노드 이름을 사전순으로 정렬한 다음 각각의 노드를 순회하면서,
        print("--" * level + node)  # 층(level)에 맞게 들여쓰기 후 노드를 출력
        print_tree(tree[node], level + 1) # 하위 노드를 재귀적으로 출력

# 트리를 만들고 출력하는 함수
def build_and_print_tree(paths):
    tree = {}   # 트리를 표현할 빈 딕셔너리를 생성(노드 이름: {하위 노드들} 형태로 트리 표현)
    for path in paths:  # 경로 정보에서 각 경로를 순회하면서,
        add_path(tree, path)    # 각 개미의 이동 경로를 트리에 추가
    print_tree(tree)    # 완성된 트리를 출력

# main
N = int(input())    # 개미가 탐색한 경로의 개수
paths = []  # 각 개미들이 이동한 경로 정보

# 각 개미의 이동 정보를 입력받아 paths 리스트에 저장
for _ in range(N):
    K, *path = input().split()
    paths.append(path)

# 모든 경로를 트리 구조로 만들어 출력
build_and_print_tree(paths)
